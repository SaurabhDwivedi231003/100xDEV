// Authentication Notes

// ---> Hashing: Converts input into a fixed-size string using a mathematical function. 
//           Used for secure password storage. Irreversible process. Common library: bcrypt.

// ---> Encryption: Encodes information for authorized access only. 
//           Reversible with the correct key. Protects sensitive data in transit. Common algorithm: AES.

// ---> JWT (JSON Web Tokens): Securely transmits information between parties as a JSON object. 
//           Digitally signed for verification and trust. Used for authentication and secure information exchange.

// ---> Local Storage: Stores key-value pairs in a web browser indefinitely. 
//           Data persists after closing the browser. Used for storing tokens or user preferences. 
//           Not recommended for sensitive data due to potential security vulnerabilities.

// ---> Session Storage: Stores key-value pairs in a web browser for the duration of the page session.

